# comparision operator
# ==,!=,<,>,<=,>=
x=10
y=20
print(x==y)
print(x!=y)
print(x<y)
print(x>y)
print(x<=y)
print(x>=y)
# logical operator
# and,or,not
x=20
y=10
print(x==20 and x>y)
print(x==20 or x<y)
print(not x!=10)
# Membership operator
# in (element present), not in(element not present)
str="hello"
print('H' in str)
l=[10,20,30]
print(20 in l)
print(40 not in l)
# identity operator
# is(work as ==),is not (work as !=) 
p=10
q=10
print(p is q , p==q )
print(p is not q, p!=q)
# Bitwise operator(perform operation on bits)
# &, |, ^(true if both are different)
e=10
# 1010
f=8
# 1000
print(bin(e))
print(bin(f))
print(e&f, bin(e&f))
print(e|f, bin(e|f))
print(e^f, bin(e^f))







