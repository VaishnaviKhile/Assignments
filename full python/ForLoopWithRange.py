# range(start, condition, updation))
# range(5) here default start=0 condition=<5 increment=1
for a in range(5):
    print(a)
for n in range(10,20):
    print(n)
for b in range(1,10,2):
    print(b)
for t in range(1,11):
    print("2*",t,"=",2*t)
# Reverse range
for b in range(10,0,-1):
    print(b)
for a in range(10,-1,-2):
    print(a)